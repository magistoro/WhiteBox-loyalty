export async function seedProfileStatuses(prisma) {
  for (const status of PROFILE_STATUS_SEEDS) {
    await prisma.profileStatus.upsert({
      where: { slug: status.slug },
      create: {
        slug: status.slug,
        title: status.title,
        description: status.description,
        rarity: status.rarity,
        icon: status.icon,
        isSystem: status.isSystem === true,
      },
      update: {
        title: status.title,
        description: status.description,
        rarity: status.rarity,
        icon: status.icon,
        isSystem: status.isSystem === true,
        isActive: true,
      },
    });
  }
}
